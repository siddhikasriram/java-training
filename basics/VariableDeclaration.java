public class VariableDeclaration{  
    public static void main(String[] args) {
        int num = 10;
        double decimal = 20.5;
        char letter = 'A';
        boolean flag = true;

        System.out.println("Integer: " + num);
        System.out.println("Double: " + decimal);
        System.out.println("Character: " + letter);
        System.out.println("Boolean: " + flag);
    }
}